	DIMENSION SPIN(80),RK(80)
	REAL ME,M1
C
	READ ( 1,* ) NMAX
	DO I=1,NMAX
	  READ ( 1,* ) N,SPIN(N),E,IPAR,RK(N)
	  WRITE ( 2,* ) N,SPIN(N),E,IPAR,RK(N)
	END DO
100	READ ( 1,*,END=200 ) M1,LAM,NL,NU,ML,MU
	CALL FCTRLS_s
	DO M=ML,MU
	  DO N=NL,NU
	    IF ( N .LE. M ) THEN
	     TR=CLEB(SPIN(N),REAL(LAM),SPIN(M),RK(N),RK(M)-RK(N),RK(M))
	     ME=SQRT(SPIN(N)*2.+1.)*TR*M1
	     IF ( ABS(ME) .GT. .000001 ) WRITE (2,*) N,M,ME,LAM
	    END IF
	  END DO
	END DO
	GO TO 100
200	CONTINUE
	STOP
	END
